/*******************************************************************************************************************************//**
 * @brief		Macro que retorna 1 o 0 segun si el bit de la variable pasada como parametro esta en 1 o 0.
 **********************************************************************************************************************************/
#define GET_BIT(variable, bitNum)	((variable >> bitNum) & 1)

/*******************************************************************************************************************************//**
 * @brief		Macro que pone en uno el bit de la variable pasada como parametro. El resto de los bits permanecen igual.
 **********************************************************************************************************************************/
#define SET_BIT(variable, bitNum)	(variable |= (1UL << bitNum))

/*******************************************************************************************************************************//**
 * @brief		Macro que pone en cero el bit de la variable pasada como parametro. El resto de los bits permanecen igual.
 **********************************************************************************************************************************/
#define CLR_BIT(variable, bitNum)	(variable &= ~(1UL << bitNum))

